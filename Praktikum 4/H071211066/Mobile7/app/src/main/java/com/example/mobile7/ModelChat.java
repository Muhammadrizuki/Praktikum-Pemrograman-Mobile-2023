package com.example.mobile7;

public class ModelChat {
    private String nama;
    private String chat;
    private String jam;

    private String jams;
    private int foto;

    private String nomor;
    private String status;
    private String tanggalStatus;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getChat() {
        return chat;
    }

    public void setChat(String chat) {
        this.chat = chat;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public int getFoto() {
        return foto;
    }

    public void setFoto(int foto) {
        this.foto = foto;
    }

    public String getJams() {
        return jams;
    }

    public void setJams(String jams) {
        this.jams = jams;
    }

    public String getNomor() {
        return nomor;
    }

    public void setNomor(String nomor) {
        this.nomor = nomor;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTanggalStatus() {
        return tanggalStatus;
    }

    public void setTanggalStatus(String tanggalStatus) {
        this.tanggalStatus = tanggalStatus;
    }
}
