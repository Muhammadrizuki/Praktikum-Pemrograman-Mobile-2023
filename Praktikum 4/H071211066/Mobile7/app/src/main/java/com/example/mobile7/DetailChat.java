package com.example.mobile7;

public class DetailChat {
    String chat;
    String jam;

    public String getChat() {
        return chat;
    }

    public void setChat(String chat) {
        this.chat = chat;
    }


    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public DetailChat(String chat, String jam) {
        this.chat = chat;
        this.jam = jam;
    }
}
